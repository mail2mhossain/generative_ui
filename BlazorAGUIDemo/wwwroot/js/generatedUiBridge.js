/**
 * generatedUiBridge.js — Pillar 3B: double-iframe sandbox for agent-generated HTML
 *
 * ## Security model — two isolation layers
 *
 *  ┌─ Blazor host (full origin) ──────────────────────────────────────────────┐
 *  │                                                                           │
 *  │  ┌─ Outer iframe  sandbox="allow-scripts"  (null origin) ──────────────┐ │
 *  │  │                                                                      │ │
 *  │  │   Shell script decodes base64 HTML → sets inner iframe srcdoc        │ │
 *  │  │                                                                      │ │
 *  │  │   ┌─ Inner iframe  sandbox="allow-scripts"  (null origin) ────────┐ │ │
 *  │  │   │                                                                │ │ │
 *  │  │   │   Agent-generated HTML runs here                               │ │ │
 *  │  │   │   Cannot access outer iframe, Blazor host, cookies,            │ │ │
 *  │  │   │   localStorage, or make credentialed requests                  │ │ │
 *  │  │   └────────────────────────────────────────────────────────────────┘ │ │
 *  │  └──────────────────────────────────────────────────────────────────────┘ │
 *  └───────────────────────────────────────────────────────────────────────────┘
 *
 * Why two layers?
 *   - Outer frame: sandbox="allow-scripts" with NO allow-same-origin gives it a
 *     null origin. It cannot read Blazor's cookies, localStorage, or DOM.
 *   - Inner frame: also sandbox="allow-scripts". Even if the generated code
 *     exploits a browser sandbox escape in the inner frame, it lands in the outer
 *     frame's null-origin context — still isolated from the Blazor host.
 *   - Permissions can only be REDUCED when crossing iframe boundaries, never
 *     increased. The inner frame cannot grant itself allow-same-origin because
 *     the outer frame doesn't have it.
 *
 * Why base64?
 *   The generated HTML is embedded as a literal string inside the shell's
 *   <script> tag. If the HTML contained </script>, it would break the shell.
 *   Base64 encoding the HTML before embedding it avoids this attack vector.
 *   encodeURIComponent + btoa (and the reverse) handles the full Unicode range.
 *
 * Exported functions:
 *   setContent(container, htmlContent)  — inject HTML into the double-iframe
 *   dispose(container)                  — remove iframes, free resources
 */

/**
 * Build and mount a double-sandboxed iframe inside `container`.
 *
 * @param {HTMLElement} container   - The Blazor ElementReference div that hosts
 *                                    the outer iframe.
 * @param {string}      htmlContent - The complete HTML document generated by
 *                                    the agent. Must be a valid HTML string.
 */
export function setContent(container, htmlContent) {
    // Remove any previously mounted iframe
    _clear(container);

    if (!htmlContent || !htmlContent.trim()) return;

    // ------------------------------------------------------------------
    // Step 1 — Base64-encode the generated HTML.
    //
    // encodeURIComponent handles all Unicode code points (e.g. emoji in
    // the generated HTML). unescape converts the %-encoded bytes to Latin-1
    // so btoa can consume them. The reverse (escape + atob) is done inside
    // the shell script.
    // ------------------------------------------------------------------
    let b64;
    try {
        b64 = btoa(unescape(encodeURIComponent(htmlContent)));
    } catch (e) {
        _showError(container, 'Failed to encode generated HTML: ' + e.message);
        return;
    }

    // ------------------------------------------------------------------
    // Step 2 — Build the outer-iframe shell HTML.
    //
    // The shell is minimal: it just decodes the base64 payload and writes
    // it to the inner iframe's srcdoc. No visible UI of its own.
    //
    // Inner iframe sandbox="allow-scripts" only — no allow-same-origin,
    // no allow-top-navigation, no allow-forms.
    // ------------------------------------------------------------------
    const shellHtml = `<!DOCTYPE html>
<html>
<head>
<meta charset="utf-8">
<style>
  * { margin: 0; padding: 0; box-sizing: border-box; }
  html, body { width: 100%; height: 100%; overflow: hidden; background: #fff; }
  #i { border: none; width: 100%; height: 100%; display: block; }
</style>
</head>
<body>
<iframe id="i" sandbox="allow-scripts" title="AI-generated content"></iframe>
<script>
  (function () {
    try {
      var html = decodeURIComponent(escape(atob('${b64}')));
      document.getElementById('i').srcdoc = html;
    } catch (e) {
      document.body.innerHTML =
        '<pre style="color:#b91c1c;padding:1rem;font-family:monospace">' +
        'Content error: ' + e.message + '</pre>';
    }
  }());
</script>
</body>
</html>`;

    // ------------------------------------------------------------------
    // Step 3 — Create the outer iframe.
    //
    // sandbox="allow-scripts" only:
    //   ✓ allow-scripts  — shell script can run to create the inner iframe
    //   ✗ allow-same-origin  — outer frame gets null origin → no cookie/
    //                          localStorage access → cannot reach Blazor host
    // ------------------------------------------------------------------
    const outer = document.createElement('iframe');
    outer.setAttribute('sandbox', 'allow-scripts');
    outer.setAttribute('title',   'AI-generated interface');
    outer.setAttribute('aria-label', 'AI-generated interface');
    outer.style.cssText = 'border:none;width:100%;height:100%;display:block;';

    // Set srcdoc to the shell — this triggers the shell to load and inject
    // the inner iframe without any network requests.
    outer.srcdoc = shellHtml;

    container.appendChild(outer);
}

/**
 * Remove all child iframes from `container` and free resources.
 * Called from GeneratedUiFrame.DisposeAsync().
 *
 * @param {HTMLElement} container
 */
export function dispose(container) {
    _clear(container);
}

// ---------------------------------------------------------------------------
// Private helpers
// ---------------------------------------------------------------------------

function _clear(container) {
    // Nullify srcdoc on every child iframe before removal so the browser
    // can reclaim the iframe's browsing context immediately.
    Array.from(container.querySelectorAll('iframe')).forEach(f => {
        f.srcdoc = '';
        f.src    = 'about:blank';
    });
    container.innerHTML = '';
}

function _showError(container, message) {
    container.innerHTML =
        '<div style="display:flex;align-items:center;gap:.5rem;padding:1rem;' +
        'color:#b91c1c;font-size:.875rem;font-family:system-ui">' +
        '<span>⚠️</span><span>' + _esc(message) + '</span></div>';
}

function _esc(str) {
    return str.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}
